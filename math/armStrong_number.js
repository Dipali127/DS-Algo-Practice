// What is Amrstrong Number?
// Armstrong Number is a number that is equal to the sum of its own digits each raised to the power of the number
// of digits.

// Examples

// Input: n = 153
// Output: true
// Explanation: 153 is an Armstrong number, 1*1*1 + 5*5*5 + 3*3*3 = 153

// Input: n = 9474
// Output: true
// Explanation: 94 + 44 + 74 + 44 = 6561 + 256 + 2401 + 256 = 9474
